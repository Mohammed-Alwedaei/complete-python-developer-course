# string slicing is used to return the index of a character

first_name = 'Sayed Mohammed'

#[start:stop:stepover]

print(first_name[0:0])

# get the first letter

print(first_name[0:1])

print(first_name[1:])

print(first_name[0:])

# stepover to skip characters

print(first_name[0::3])

# to reverse a string

print(first_name[::-1])